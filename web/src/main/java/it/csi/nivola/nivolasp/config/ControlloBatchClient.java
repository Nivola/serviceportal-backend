package it.csi.nivola.nivolasp.config;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.remoting.httpinvoker.HttpInvokerProxyFactoryBean;

import it.csi.nivola.nivolasp.remote.service.ControlloBatchService;

@Configuration
public class ControlloBatchClient {
	
	@Autowired
	ApplicationProperties applicationProperties;

    @Bean
    public HttpInvokerProxyFactoryBean invoker() {
        HttpInvokerProxyFactoryBean invoker = new HttpInvokerProxyFactoryBean();
        invoker.setServiceUrl(applicationProperties.getDeploy().getEndpointControlloBatch());
        invoker.setServiceInterface(ControlloBatchService.class);
        return invoker;
    }
}
