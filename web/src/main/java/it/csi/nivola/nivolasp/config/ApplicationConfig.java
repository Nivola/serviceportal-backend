package it.csi.nivola.nivolasp.config;

import java.util.ArrayList;
import java.util.List;

import org.springframework.cache.Cache;
import org.springframework.cache.CacheManager;
import org.springframework.cache.annotation.EnableCaching;
import org.springframework.cache.concurrent.ConcurrentMapCache;
import org.springframework.cache.support.SimpleCacheManager;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;

@Configuration
@EnableCaching
public class ApplicationConfig {

	//cache per le metriche
    @Bean
    public CacheManager cacheManager() {
        SimpleCacheManager cacheManager = new SimpleCacheManager();
        List<Cache> caches = new ArrayList<Cache>();
        caches.add(new ConcurrentMapCache("metrichePortal"));
        caches.add(new ConcurrentMapCache("metricheRegolePortal"));
        caches.add(new ConcurrentMapCache("metricheListino"));
        cacheManager.setCaches(caches);
        return cacheManager;
    }

    //aggiungere qui altre configurazioni se serve
}
